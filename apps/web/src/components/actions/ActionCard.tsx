export interface ActionCardProps {
  /** The action definition */
  action: AgentAction;
  /** Current state of the action */
  state?: ActionState;
  /** Whether auto-trigger is enabled */
  autoTriggerEnabled?: boolean;
  /** Cooldown remaining in milliseconds */
  cooldownRemaining?: number;
  /** Callback when action is triggered */
  onTrigger?: (actionId: string) => void;
  /** Callback when auto-trigger is toggled */
  onToggleAutoTrigger?: (actionId: string, enabled: boolean) => void;
  /** Additional CSS classes */
  className?: string;
}

/**
 * Get color class for token estimate badge
 */
function getTokenBadgeVariant(
  estimate: TokenEstimate
): "teal" | "purple" | "warning" {
  switch (estimate) {
    case "low":
      return "teal";
    case "medium":
      return "purple";
    case "high":
      return "warning";
  }
}

/**
 * Get state indicator color
 */
function getStateColor(state: ActionState): string {
  switch (state) {
    case "idle":
      return "bg-text-muted";
    case "queued":
      return "bg-warning";
    case "processing":
      return "bg-teal animate-pulse";
    case "reviewing":
      return "bg-purple";
    case "success":
      return "bg-success";
    case "failed":
      return "bg-error";
    case "cancelled":
      return "bg-text-muted";
    case "committed":
      return "bg-success";
    case "discarded":
      return "bg-text-muted";
    default:
      return "bg-text-muted";
  }
}

